    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        // TODO add your handling code here:
        
        Conexion_Bueno conexion_bueno = new Conexion_Bueno();
        Datos_Bueno datos_bueno = new Datos_Bueno();
        datos_bueno.setNombre(txtnombre.getText());
        datos_bueno.setTelefono(Integer.valueOf(txttelefono.getText()));
        datos_bueno.setCelular(Integer.valueOf(txtcelular.getText()));
        datos_bueno.setDireccion(txtdireccion.getText());
        btngrabar.setText("Grabar");
        if (accion.equals("I")){
        if(conexion_bueno.insertar(datos_bueno)){
            JOptionPane.showMessageDialog(this, "Se grabo correctamente...");
        }else{
            JOptionPane.showMessageDialog(this, "Error al grabar...");
        }
        }else if (accion.equals("A")) {
            datos_bueno.setId(valorID);
            if(conexion_bueno.actualizar(datos_bueno)){
                JOptionPane.showMessageDialog(null, "Se actualizo correctamente","Actualizacion",JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar","Actualizacion",JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_btngrabarActionPerformed
        CargarDatos();
    }
    
    
