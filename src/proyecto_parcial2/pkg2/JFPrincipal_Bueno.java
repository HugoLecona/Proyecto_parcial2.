   private void CargarDatos(){
       //Para limpiar el modelo
       mimodelo = new DefaultTableModel(null,titulo);
       //Para recargar la tabla cada vez que se modifique algo
       Conexion_Bueno conexion_bueno = new Conexion_Bueno();
        if (conexion_bueno.consultartodos()){
            listadatos_bueno = conexion_bueno.getListDatos_Bueno();
            for(Datos_Bueno datos_bueno : listadatos_bueno){
               mimodelo.addRow(new Object[]{String.valueOf(datos_bueno.getId()),datos_bueno.getNombre(),String.valueOf(datos_bueno.getTelefono()),String.valueOf(datos_bueno.getCelular()),datos_bueno.getDireccion()});
            }
            tabladata.setModel(mimodelo);
        } else {
            JOptionPane.showMessageDialog(this, "No se pueden cargar los datos....");
            
        }
   }
    
    
